void main() {
  print("LOOPING PERTAMA"); // Mencetak pesan "LOOPING PERTAMA" ke layar.

  int i = 1; // Inisialisasi variabel i dengan nilai 1.
  while (i <= 10) { // Memulai perulangan while dengan kondisi i kurang dari atau sama dengan 10.
    print("$i-i love her"); // Mencetak nilai i dan pesan "i love her".
    i++; // Menambahkan nilai i sebesar 1.
  }

  print("LOOPING KEDUA"); // Mencetak pesan "LOOPING KEDUA" ke layar.

  int j = 10; // Inisialisasi variabel j dengan nilai 10.
  while (j >= 1) { // Memulai perulangan while dengan kondisi j lebih besar dari atau sama dengan 1.
    print("$j-she not love me"); // Mencetak nilai j dan pesan "she not love me".
    j--; // Mengurangkan nilai j sebesar 1.
  }
}
