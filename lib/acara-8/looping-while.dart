void main() {
  print("LOOPING PERTAMA"); // Mencetak pesan "LOOPING PERTAMA" ke layar.

  int i = 1; // Inisialisasi variabel i dengan nilai 1.
  while (i <= 10) {
    // Memulai perulangan while dengan kondisi i kurang dari atau sama dengan 10.
    print("$i - Saya suka sama kamu"); // Mencetak nilai i dan pesan "Saya suka sama kamu".
    i++; // Menambahkan nilai i sebesar 1.
  }

  print("LOOPING KEDUA"); // Mencetak pesan "LOOPING KEDUA" ke layar.

  int j = 10; // Inisialisasi variabel j dengan nilai 10.
  while (j >= 1) {
    // Memulai perulangan while dengan kondisi j lebih besar dari atau sama dengan 1.
    print("$j - Saya tidak suka sama kamu"); // Mencetak nilai j dan pesan "Saya tidak suka sama kamu".
    j--; // Mengurangkan nilai j sebesar 1.
  }
}
