void main() {
  var angka = 100;
  print(angka == 100); // Ini mencetak hasil perbandingan apakah nilai variabel angka sama dengan 100. Hasilnya adalah true, karena angka sama dengan 100.
  print(angka == 20);  // Ini mencetak hasil perbandingan apakah nilai variabel angka sama dengan 20. Hasilnya adalah false, karena angka tidak sama dengan 20.
}
