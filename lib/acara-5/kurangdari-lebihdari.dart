void main() {
  var number = 17;

  print(number < 20);   // Ini mencetak hasil apakah nilai variabel number kurang dari 20. Hasilnya adalah true, karena 17 kurang dari 20.
  print(number > 17);   // Ini mencetak hasil apakah nilai variabel number lebih dari 17. Hasilnya adalah false, karena 17 tidak lebih dari 17.
  print(number >= 17);  // Ini mencetak hasil apakah nilai variabel number lebih besar dari atau sama dengan 17. Hasilnya adalah true, karena 17 sama dengan 17.
  print(number <= 20);  // Ini mencetak hasil apakah nilai variabel number kurang dari atau sama dengan 20. Hasilnya adalah true, karena 17 kurang dari 20.
}
