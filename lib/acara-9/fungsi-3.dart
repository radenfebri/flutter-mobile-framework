  // Memanggil fungsi kalikanDua() dengan argumen 6
  print(kalikanDua(6)); 
}
// Ini adalah fungsi yang menerima satu argumen 'angka' dan mengembalikan hasil perkalian 'angka' dengan 2.
int kalikanDua(int angka) {
  return angka * 2; // Mengembalikan hasil perkalian 'angka' dengan 2.
}
