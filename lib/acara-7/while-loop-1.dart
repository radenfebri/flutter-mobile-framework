void main() {
  var flag = 1; // Inisialisasi variabel 'flag' dengan nilai awal 1
  
  while (flag < 10) { // Perulangan while dengan kondisi 'flag' kurang dari 10
    print("iterasi ke" + flag.toString()); // Cetak pesan iterasi dengan nilai 'flag'
    flag++; // Mengubah nilai 'flag' dengan menambahkan 1
  }
}
